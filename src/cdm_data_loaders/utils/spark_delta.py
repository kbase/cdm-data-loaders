"""Utilities for interacting with Spark and delta tables."""

from berdl_notebook_utils.setup_spark_session import get_spark_session
from berdl_notebook_utils.spark.database import create_namespace_if_not_exists
from pyspark.sql import DataFrame, DataFrameWriter, SparkSession

from cdm_data_loaders.utils.cdm_logger import get_cdm_logger

logger = get_cdm_logger()

APPEND = "append"
OVERWRITE = "overwrite"
ERROR = "error"  # default
ERROR_IF_EXISTS = "errorifexists"
IGNORE = "ignore"

WRITE_MODE = [APPEND, OVERWRITE, ERROR, ERROR_IF_EXISTS, IGNORE]

DEFAULT_APP_NAME = "cdm_data_loader"
DEFAULT_NAMESPACE = "default"


def get_spark(
    app_name: str | None = None,
    **kwargs,
) -> SparkSession:
    """Set up a Spark session and namespace.

    If no app_name is provided, the function will use the string "cdm_data_loader".

    :param app_name: name for the app (used for the Spark session), defaults to None
    :type app_name: str, optional
    :param kwargs: extra arguments to supply to the spark session
    :type kwargs: dict[str, Any] | None
    :return: a Spark session
    :rtype: SparkSession
    """
    return get_spark_session(app_name or DEFAULT_APP_NAME, **kwargs)


def set_up_workspace(
    app_name: str | None = None,
    namespace: str | None = None,
    tenant_name: str | None = None,
    data_dir: str | None = None,
    **kwargs: str | int | bool | dict | None,
) -> tuple[SparkSession, str]:
    """Set up a Spark session and namespace.

    If no app_name is provided, the function will use the string "cdm_data_loader".

    N.b. this is the only function where the "raw" namespace and tenant are specified. All other functions use
    the delta_ns namespace generated by berdl_notebook_utils.

    :param app_name: name for the app (used for the Spark session), defaults to None
    :type app_name: str, optional
    :param namespace: namespace for data I/O, defaults to None
    :type namespace: str, optional
    :param tenant_name: name of the tenant, if applicable; defaults to None
    :type tenant_name: str, optional
    :param data_dir: directory in which to save data, if applicable; defaults to None. NOT IMPLEMENTED.
    :type data_dir: str, optional
    :param kwargs: extra arguments to supply to the spark session
    :type kwargs: dict[str, Any] | None
    :return: a Spark session and the BERDL-approved namespace
    :rtype: tuple[SparkSession, str]
    """
    # TODO (?): implement data_dir
    if data_dir:
        msg = "The data_dir parameter has not been implemented."
        logger.error(msg)
        raise NotImplementedError(msg)
    spark = get_spark(app_name or DEFAULT_APP_NAME, **kwargs)
    # if the namespace has already been created, this will return the doctored namespace
    delta_ns = create_namespace_if_not_exists(spark, namespace or DEFAULT_NAMESPACE, tenant_name=tenant_name)
    return (spark, delta_ns)


def get_existing_database_save_dir(spark: SparkSession, delta_ns: str) -> str | None:
    """Check whether a table is already registered in the Hive metastore, and if so, where data is saved.

    :param spark: spark session
    :type spark: SparkSession
    :param delta_ns: delta_ns name
    :type delta_ns: str
    :raises ValueError: if the table exists but the SQL query to retrieve the description returns unexpected results
    :return: path to the existing table data or None
    :rtype: str | None
    """
    if not spark.catalog.databaseExists(delta_ns):
        return None

    # namespace query
    ns_location_rows = spark.sql(f"DESCRIBE NAMESPACE EXTENDED {delta_ns}").collect()
    # we want info_value
    ns_locations = [r["info_value"] for r in ns_location_rows if r["info_name"].lower() == "location"]

    if len(ns_locations) == 1:
        return ns_locations[0]

    # there should only be one 'location' entry, so more than one indicates some sort of dodgy data.
    msg = f"Expected 1 location row in {delta_ns} description; got {len(ns_locations)} rows."
    logger.error(msg)
    logger.error(ns_locations)
    raise ValueError(msg)


def get_existing_table_save_dir(spark: SparkSession, delta_ns: str, table: str | None = None) -> str | None:
    """Check whether a table is already registered in the Hive metastore, and if so, where data is saved.

    :param spark: spark session
    :type spark: SparkSession
    :param db_table: delta_ns.table name
    :type db_table: str
    :raises ValueError: if the table exists but the SQL query to retrieve the description returns unexpected results
    :return: path to the existing table data or None
    :rtype: str | None
    """
    db_table = f"{delta_ns}.{table}"
    if not spark.catalog.tableExists(db_table):
        return None

    table_location_rows = spark.sql(f"DESCRIBE EXTENDED {db_table}").where('col_name = "Location"').collect()
    table_locations = [r["data_type"] for r in table_location_rows if r["col_name"].lower() == "location"]
    if len(table_locations) == 1:
        return table_locations[0]

    # there should only be one 'location' entry, so more than one indicates some sort of dodgy data.
    msg = f"Expected 1 location row in {db_table} description; got {len(table_locations)} rows."
    logger.error(msg)
    logger.error(table_locations)
    raise ValueError(msg)


def write_delta(spark: SparkSession, sdf: DataFrame, delta_ns: str, table: str, mode: str = "append") -> None:
    """
    Write data as delta tables and register it in the hive metastore.

    :param spark: spark sesh
    :type spark: SparkSession
    :param sdf: data frame to be written
    :type sdf: DataFrame
    :param delta_ns: namespace assigned by `set_up_workspace` or `create_namespace_if_not_exists`
    :type delta_ns: str
    :param table: table name
    :type table: str
    :param mode: spark write mode (one of append, overwrite, ignore, error, errorifexists)
    :type mode: str
    """
    # Write Spark DataFrame in Delta format and register it with the Hive metastore.

    if mode not in WRITE_MODE:
        msg = f"Invalid mode supplied for writing delta table: {mode}"
        logger.error(msg)
        raise ValueError(msg)

    db_table = f"{delta_ns}.{table}"
    if sdf is None or not isinstance(sdf, DataFrame) or sdf.isEmpty():
        logger.warning("No data to write to %s", db_table)
        return

    # this should have been set using create_namespace_if_not_exists
    base_path = get_existing_database_save_dir(spark, delta_ns)
    if not base_path:
        msg = "Could not find an appropriate base directory for saving data. Was the workspace initialised using set_up_workspace?"
        logger.error(msg)
        raise RuntimeError(msg)

    # check whether the table already exists and get the path if so
    existing_path = get_existing_table_save_dir(spark, delta_ns, table)

    if existing_path and mode in (IGNORE, ERROR, ERROR_IF_EXISTS):
        # TODO: check if data must be in the dir or whether the table just needs to be registered
        logger.warning(
            "Database table %s already exists and writer is set to %s mode, so no data would be written. Aborting.",
            db_table,
            mode,
        )
        return

        """
        # TODO: implement check for non-standard table paths
        # TODO: write tests
        if existing_path:
            table_path = f"{base_path}/{table}"

            existing = urlparse(existing_path)
            proposed = urlparse(table_path)

            if existing.path != proposed.path:
                logger.warning(
                    "Existing path does not match the projected base path for the table. Data written to this directory must be tracked manually."
                )
        """
    merge_or_overwrite_schema = "mergeSchema" if mode == APPEND else "overwriteSchema"
    # use to(schema) to ensure that the schema is saved with the dataframe
    writer = sdf.to(sdf.schema).write.format("delta").mode(mode).option(merge_or_overwrite_schema, "true")

    logger.info("Writing table %s in mode %s (rows=%d)", db_table, mode, sdf.count())
    logger.debug(sdf.printSchema())
    logger.debug(sdf.show(10, truncate=False))

    # use the standard spark saveAsTable, which will save to the appropriate subdirectory under the
    # designated namespace dir
    try:
        writer.saveAsTable(db_table)
        logger.info("Saved managed table %s (rows=%d)", db_table, sdf.count())
    except Exception:
        logger.exception("Error writing managed table %s", db_table)
        raise


def write_delta_to_file(
    spark: SparkSession, sdf: DataFrame, delta_ns: str, table: str, writer: DataFrameWriter, data_dir: str
) -> None:
    """Write delta tables to a specific location."""
    db_table = f"{delta_ns}.{table}"
    try:
        writer.save(data_dir)
        # Register/create an external table using LOCATION
        spark.sql(f"CREATE TABLE IF NOT EXISTS {db_table} USING DELTA LOCATION '{data_dir}'")
        logger.info("Saved external table %s (rows=%d) to %s", db_table, sdf.count(), data_dir)
    except Exception:
        logger.exception("Error writing external table %s", db_table)
        raise


def preview_or_skip(spark: SparkSession, delta_ns: str, table: str, limit: int = 20) -> None:
    """
    Preview table if it exists.
    """
    db_table = f"{delta_ns}.{table}"
    if not spark.catalog.tableExists(db_table):
        logger.info("Table %s not found. Skipping preview.", db_table)
        return

    logger.info("Preview for %s:", db_table)
    spark.sql(f"SELECT * FROM {db_table} LIMIT {limit}").show(truncate=False)
